import { openPostgresClient } from "./lib/clients.mjs";

const CREATE_SCHEMA_SQL = `
CREATE TABLE IF NOT EXISTS role_types (
  id INTEGER PRIMARY KEY,
  name TEXT UNIQUE NOT NULL CHECK (char_length(name) <= 15)
);

CREATE TABLE IF NOT EXISTS order_status_types (
  id INTEGER PRIMARY KEY,
  name TEXT UNIQUE NOT NULL CHECK (char_length(name) <= 15)
);

CREATE TABLE IF NOT EXISTS users (
  id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  email TEXT UNIQUE NOT NULL,
  password TEXT NOT NULL,
  role TEXT NOT NULL,
  role_id INTEGER REFERENCES role_types(id),
  display_name TEXT,
  street TEXT,
  city TEXT,
  postal_code TEXT,
  country TEXT
);

CREATE TABLE IF NOT EXISTS catalog_items (
  id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  public_id TEXT UNIQUE,
  sku TEXT UNIQUE NOT NULL,
  name TEXT NOT NULL,
  header TEXT,
  description TEXT NOT NULL,
  price_cents INTEGER NOT NULL
);

CREATE TABLE IF NOT EXISTS orders (
  id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  public_order_id TEXT UNIQUE,
  user_id INTEGER NOT NULL REFERENCES users(id),
  shipping_street TEXT,
  shipping_city TEXT,
  shipping_postal_code TEXT,
  shipping_country TEXT,
  payment_name_on_card TEXT,
  total_cents INTEGER NOT NULL,
  payment_last4 TEXT NOT NULL,
  created_at TIMESTAMPTZ NOT NULL,
  order_status_type_id INTEGER REFERENCES order_status_types(id),
  cancellation_reason TEXT
);

CREATE TABLE IF NOT EXISTS order_items (
  id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  order_id INTEGER NOT NULL REFERENCES orders(id),
  catalog_item_id INTEGER NOT NULL REFERENCES catalog_items(id),
  quantity INTEGER NOT NULL,
  unit_price_cents INTEGER NOT NULL
);

CREATE INDEX IF NOT EXISTS idx_users_role_id ON users(role_id);
CREATE UNIQUE INDEX IF NOT EXISTS idx_catalog_items_public_id ON catalog_items(public_id);
CREATE UNIQUE INDEX IF NOT EXISTS idx_orders_public_order_id ON orders(public_order_id);
CREATE INDEX IF NOT EXISTS idx_orders_status_type_id ON orders(order_status_type_id);
CREATE INDEX IF NOT EXISTS idx_orders_user_created_at ON orders(user_id, created_at DESC, id DESC);
`;

async function main() {
  const client = await openPostgresClient();
  try {
    await client.query("BEGIN");
    await client.query(CREATE_SCHEMA_SQL);
    await client.query("COMMIT");
    console.log("Postgres target schema is ready.");
  } catch (error) {
    await client.query("ROLLBACK");
    throw error;
  } finally {
    await client.end();
  }
}

main().catch((error) => {
  console.error("Failed to apply Postgres schema:", error.message);
  process.exitCode = 1;
});
